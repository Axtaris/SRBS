     private void llenarComboBox(){
       ControladorTipo_Equipo tipo = new ControladorTipo_Equipo();
        rstipoequ = tipo.nombre();
       ControladorUsuario usuario = new ControladorUsuario();
        rstipous = usuario.nombre();
       ControladorEstado estado = new ControladorEstado();
        rstipoestado = estado.nombre();
        
        
        try {
            while(rstipoequ.next()){
                tipos.addItem(rstipoequ.getString("nombre"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Agregar_Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            while(rstipous.next()){
                usuarios.addItem(rstipous.getString("nombre"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Agregar_Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {              
            while(rstipoestado.next()){
                estados.addItem(rstipoestado.getString("nombre"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Agregar_Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
