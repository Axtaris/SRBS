private void llenarComboBox(){
        ControladorUsuario control = new ControladorUsuario();
        rsusuario = control.nombreusuario();
       
        try {
            while(rsusuario.next()){
                jComboBox1.addItem(rsusuario.getString("nombre")+" "+rsusuario.getString("apellido"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Agregar_Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
}      
     public void llenarTabla2(int id){
        tabla2.setModel(dfm);
         
        dfm.setColumnIdentifiers(new Object[]{"ID","EQUIPO","REGISTRO"});
        
        ControladorEquipo control = new ControladorEquipo();
        
        rs = control.traerEquipos(id);
        
        try {
            while(rs.next()){
                dfm.addRow(new Object[]{rs.getInt("id"),rs.getString("equipo"),""});
            }   
        } catch (SQLException ex) {
            Logger.getLogger(Vista_Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        TableColumn registrar = tabla2.getColumnModel().getColumn(2);
        JComboBox combo1 = new JComboBox();
        combo1.addItem("Si");
        combo1.addItem("No");
        registrar.setCellEditor(new DefaultCellEditor(combo1));
      
    }
     
