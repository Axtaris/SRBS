    private void llenarComboBox(){
        ControladorCentro ccentro = new ControladorCentro();
        rscentro = ccentro.nombre();
        
        ControladorTipo_Usuario ctipou_suario = new ControladorTipo_Usuario();
        rstipous = ctipou_suario.nombre();
        
        ControladorTipo_Documento ctipo_documento = new ControladorTipo_Documento();
        rstipodoc = ctipo_documento.nombre();
        
        try {
            while(rscentro.next()){
                jComboBox1.addItem(rscentro.getString("abreviatura"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Agregar_Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            while(rstipous.next()){
                jComboBox2.addItem(rstipous.getString("nombre"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Agregar_Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {              
            while(rstipodoc.next()){
                jComboBox3.addItem(rstipodoc.getString("abreviatura"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Agregar_Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
